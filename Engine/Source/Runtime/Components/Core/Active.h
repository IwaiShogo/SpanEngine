#pragma once

namespace Span
{
	/**
	 * @struct	Active
	 * @brief	✅ エンティティの活動状態を制御するコンポーネント。
	 * @details
	 * false の場合、このエンティティに関連するシステム (描画、物理、更新処理) はスキップされます。
	 */
	struct Active
	{
		bool IsActive = true;

		Active() = default;
		Active(bool isActive) : IsActive(isActive) {}
	};
}

